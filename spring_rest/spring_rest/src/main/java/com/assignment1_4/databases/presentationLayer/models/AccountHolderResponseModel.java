package com.assignment1_4.databases.presentationLayer.models;

import com.assignment1_4.databases.dataLayer.models.Address;
import com.assignment1_4.databases.enumsNew.Gender;

public class AccountHolderResponseModel {

    private Long id;
    private String firstName;
    private String lastName;
    private Gender gender;
    private Address address;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public Gender getGender() {
        return gender;
    }

    public void setGender(Gender gender) {
        this.gender = gender;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }
}
